<template>
  <form id="todo-form">
    <input
      type="text"
      id="todo-input"
      placeholder="Enter a new task"
      v-model="msg"
    />
    <button type="button" @click="todoInput">Add</button>
  </form>
</template>

<script>
export default {
  data() {
    return {
      msg: "",
    };
  },
  methods: {
    todoInput() {
      this.$emit("todoInput", this.msg);
      this.msg = "";
    },
  },
};
</script>
